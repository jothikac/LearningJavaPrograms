package Ex_13072024;

public class Lab045 {
    public static void main(String[] args) {

        System.out.println(10 == 10); // == //true // Compare two values
        System.out.println(10 >= 10); // 10 > 10 or 10 = 10 // true
        System.out.println(10 <= 10); // 10 < 10 or 10 = 10 // true
        System.out.println(10 > 10);  // false
        System.out.println(10 < 10); // false

    }
}
