package Ex_13072024;

public class Lab041 {
    public static void main(String[] args) {
        // Compound Assignment Operators
         int a = 10;
         // +=, -=, /=, *=,%=
        //+= ->
        a+=10; // same as a = a+10 // 20
        a-=10; // same as a = a-10 // 0
        a*=10; // same as a = a*10 // 100
        a/=10; // same as a = a/10 // 1
        a%=10; // same as a = a%10 // 0
        // System.out.println(a);


    }
}
