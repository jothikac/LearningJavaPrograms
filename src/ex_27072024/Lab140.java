public class Lab140 {
    public static void main(String[] args) {
        int[] numbers = {10,20,30,40,50};
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]*2);
            // i = 0 -> i = 10  -> 20
            // i = 1 -> i = 20  -> 40
            // i = 2 -> i = 30  -> 60
            // i = 3 -> i = 40  -> 80
            // i = 4 -> i = 50  -> 100

        }
    }
}
